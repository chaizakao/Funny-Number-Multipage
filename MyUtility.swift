import Foundation

class MyUtillity  {
    
    func increaseNumber(strNumber: String) -> String {
        var  numberInt = Int(strNumber)
        numberInt = numberInt! + 1
        return String(describing: numberInt)
        
        
        
        
    }
    
    
    
    
    
    
    
    
    
    
